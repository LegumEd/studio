const sidebarMenuButtonVariants = cva(
  "group/button flex w-full items-center gap-3 rounded-md px-3 py-2 text-left text-sm font-medium outline-none ring-sidebar-ring transition-colors hover:bg-sidebar-accent hover:text-sidebar-accent-foreground focus-visible:ring-2 disabled:pointer-events-none disabled:opacity-50",
  {
    variants: {
        isActive: {
            true: "bg-sidebar-accent text-sidebar-accent-foreground",
            false: "text-sidebar-foreground"
        }
    },
    defaultVariants: {
        isActive: false
    }
  }
)

const SidebarMenuButton = React.forwardRef<
  HTMLButtonElement,
  React.ComponentProps<"button"> & {
    asChild?: boolean
    isActive?: boolean
    tooltip?: string | React.ComponentProps<typeof TooltipContent>
  } & VariantProps<typeof sidebarMenuButtonVariants>
>(
  (
    {
      asChild = false,
      isActive = false,
      tooltip,
      className,
      children,
      ...props
    },
    ref
  ) => {
    const Comp = asChild ? Slot : "button"
    const { isMobile, state } = useSidebar()

    const buttonChildren = (
        <>
            {React.Children.map(children, (child) => {
                 if (React.isValidElement(child) && child.type === "span") {
                    return React.cloneElement(child as React.ReactElement, {
                        className: cn("truncate transition-opacity duration-200", state === "collapsed" && "opacity-0 w-0")
                    })
                }
                if (React.isValidElement(child) && child.props.hasOwnProperty('icon')) {
                     return React.cloneElement(child as React.ReactElement, {
                        className: cn("h-5 w-5 shrink-0")
                    })
                }
                return child
            })}
        </>
    )

    const button = (
      <Comp
        ref={ref}
        data-sidebar="menu-button"
        data-active={isActive}
        className={cn(sidebarMenuButtonVariants({isActive}), state === 'collapsed' && 'justify-center', className)}
        {...props}
      >
        {children}
      </Comp>
    )

    if (!tooltip) {
      return button
    }

    if (typeof tooltip === "string") {
      tooltip = {
        children: tooltip,
      }
    }

    return (
      <Tooltip>
        <TooltipTrigger asChild>{button}</TooltipTrigger>
        <TooltipContent
          side="right"
          align="center"
          hidden={state !== "collapsed" || isMobile}
          {...tooltip}
        />
      </Tooltip>
    )
  }
)
SidebarMenuButton.displayName = "SidebarMenuButton"

